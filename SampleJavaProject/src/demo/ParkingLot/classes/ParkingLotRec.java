package demo.ParkingLot.classes;

public class ParkingLotRec {
    public int parkingNumber;
    public boolean parkingFlag;

    public int getParkingNumber() {
        return parkingNumber;
    }

    public void setParkingNumber(int parkingNumber) {
        this.parkingNumber = parkingNumber;
    }

    public boolean isParkingFlag() {
        return parkingFlag;
    }

    public void setParkingFlag(boolean parkingFlag) {
        this.parkingFlag = parkingFlag;
    }
}
